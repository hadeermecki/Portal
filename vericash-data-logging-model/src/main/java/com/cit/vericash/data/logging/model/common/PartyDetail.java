package com.cit.vericash.data.logging.model.common;

import java.io.Serializable;

import javax.persistence.Embeddable;

@Embeddable
public class PartyDetail implements Serializable {
	private static final long serialVersionUID = -2184056554692894306L;
	private String userKey;
	private String msisdn;
	private String walletShortCode;
	private Long type;
	private Long account;
	public String getUserKey() {
		return userKey;
	}
	public void setUserKey(String userKey) {
		this.userKey = userKey;
	}
	public String getMsisdn() {
		return msisdn;
	}
	public void setMsisdn(String msisdn) {
		this.msisdn = msisdn;
	}
	public String getWalletShortCode() {
		return walletShortCode;
	}
	public void setWalletShortCode(String walletShortCode) {
		this.walletShortCode = walletShortCode;
	}
	public Long getType() {
		return type;
	}
	public void setType(Long type) {
		this.type = type;
	}
	public Long getAccount() {
		return account;
	}
	public void setAccount(Long account) {
		this.account = account;
	}
	@Override
	public String toString() {
		return "PartyDetail [userKey=" + userKey + ", msisdn=" + msisdn
				+ ", walletShortCode=" + walletShortCode + "]";
	}
}
